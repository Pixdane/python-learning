def product(*numbers):
    s = 1
    if numbers == ():
        raise TypeError('请输入至少一个参数！')
    for x in numbers:
        s = s * x
    return s
print('product(5) =', product(5))
print('product(5, 6) =', product(5, 6))
print('product(5, 6, 7) =', product(5, 6, 7))
print('product(5, 6, 7, 9) =', product(5, 6, 7, 9))
if product(5) != 5:
    print('测试失败!')
elif product(5, 6) != 30:
    print('测试失败!')
